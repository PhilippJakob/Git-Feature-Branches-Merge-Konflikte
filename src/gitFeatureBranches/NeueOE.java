package gitFeatureBranches;

public class NeueOE
{
   public class OE
   {
      String Name;
      int ID;
      public OE(String name, int iD)
      {
   	  super();
   	  Name = name;
   	  ID = iD;
      }
      public String getName()
      {
         return Name;
      }
      public void setName(String name)
      {
         Name = name;
      }
      public int getID()
      {
         return ID;
      }
      public void setID(int iD)
      {
         ID = iD;
      }

   }
   
   
}
