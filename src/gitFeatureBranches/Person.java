package gitFeatureBranches;

public class Person
{
   String Name;
   int ID;
   public Person(String name, int iD)
   {
	  super();
	  Name = name;
	  ID = iD;
   }
   public String getName()
   {
      return Name;
   }
   public void setName(String name)
   {
      Name = name;
   }
   public int getID()
   {
      return ID;
   }
   public void setID(int iD)
   {
      ID = iD;
   }

}
